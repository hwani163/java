package step05;

/* main() 메서드 
- JVM이 클래스를 실행할 때 제일 처음에 호출하는 메서드이다.
- 반드시 다음과 같은 형식으로 정의되어야 한다.
public static void main(String[] 변수명) { ... }
main() 메서드의 "변수명"은 개발 마음대로 이름을 지을 수 있다.
- main() 메서드가 없는 클래스는 JVM이 직접 실행할 수 없다.
*/
public class Test04 {

  public static void main(String[] args) {
    System.out.println("main() 메서드가 호출됨...");

  }

}
