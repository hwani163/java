package step09.ex4;
/**
 * 기존 코드를 변경하지 않고 기능을 추가시키는 방법 =>상속(inheritance)
 * 
 * 요구사항?
 * 기존의 Score 클래스는 국영수 점수만 관리 하였다.
 * 여기에 과학과 사회를 추가하고 싶다!
 * 
 * 해결책
 *  ->기존의 Score클래스를 바꾼다.
 *  =>총점과 평균을 구하는 방식도 바꿔야 한다.
 *  =>국 , 영 , 수 점수만 관리하고 싶은 사람은 어떻게 해야할까?
 *  =>이미 이 클래스로 작업한 프로그램은 어떻게 할까?
 *  => 기존 Score를 그대로 두고 새로운 기능이 있는 별도의 클래스를 만들어 추가한다.
 *  => 기존의 기능은 어떻게 이용하는가?
 *  =>이것을 해결 하기 위해 등장한 문법이  "상속"이다*/
public class Test {
  public static void main(String[] args) {
    
    ScoreEx s1 = new ScoreEx();
    s1.setName("홍길동");
    s1.setKor(100);     //아빠클래스에 있는 메서드
    s1.setEng(100);     //아빠클래스에 있는 메서드
    s1.setMath(100);   //아빠클래스에 있는 메서드
    s1.setSoc(80);
    s1.setSci(70);
    
//    문제점 : 추가한 과목에 대하여 계산할 때(print , compute) 반영하여야 한다.
//    출력할때도 추가한 과목을 표시하여야 한다.
//    그런데 compute()와 print()는 Score의 method이기 때문에 
//    우리가 원하는 대로 동작하지 않는다.
    
//    해결책! 다음주에!
    s1.print();
  }
}
