package step03;
/*
 * 비교(관계)연산자
 * 
 * < , <= , > , >= , ==
 *  - 연산결과는 논리값이다 즉! True or False
 * 
 * */

public class Test06 {

  public static void main(String[] args) {
    
    System.out.println(10 < 11);
    System.out.println(10 > 10);
    System.out.println(10 <= 10);
    System.out.println(10 >= 10);
    System.out.println(10 == 10);
    System.out.println(10 == 11);
    System.out.println(10 != 11);
    
    System.out.println(10L < 11L);
    System.out.println(10.2f < 10.1119f);
    System.out.println(10.2 < 10.199999);
    
    
    
    
    
    
  }

}
