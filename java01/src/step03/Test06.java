package step03;

/*
 * 비교(관계) 연산자
 * - <, <=, >, >=, ==
 * - 연산 결과는 논리 값이다. true/false
 */
public class Test06 {

  public static void main(String[] args) {
    System.out.println(10 < 11);
    System.out.println(10 > 11);
    System.out.println(10 <= 10);
    System.out.println(10 >= 10);
    System.out.println(10 == 11);
    System.out.println(10 == 10);
    System.out.println(10 != 11);
    
    System.out.println(10L < 11L);
    System.out.println(10.2f < 10.1999f);
    System.out.println(10.2 < 10.1999);
    
  }

}











